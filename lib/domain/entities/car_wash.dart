class CarWash {
  final String id;
  final String name;
  final String address;
  final double rating;
  final int reviewCount;
  final double latitude;
  final double longitude;

  const CarWash({
    required this.id,
    required this.name,
    required this.address,
    required this.rating,
    required this.reviewCount,
    required this.latitude,
    required this.longitude,
  });
}